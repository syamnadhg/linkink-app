// Filter functions
export const filterUsers = (users, filters) => {
  return users.filter(user => {
    if (filters.distance !== 'all' && user.distance > parseInt(filters.distance)) {
      return false
    }
    if (filters.gender !== 'all' && user.gender !== filters.gender) {
      return false
    }
    if (filters.interests !== 'all' && user.interests !== filters.interests) {
      return false
    }
    return true
  })
}

export const filterPosts = (posts, filters) => {
  return posts.filter(post => {
    if (filters.gender !== 'all' && post.gender !== filters.gender) {
      return false
    }
    if (filters.interests !== 'all' && post.interests !== filters.interests) {
      return false
    }
    return true
  })
}

